const modal = document.getElementById('modal');
const modalGallery = document.getElementById('modal-gallery');
const closeModal = document.querySelector('.close');
const prevButton = document.getElementById('prev');
const nextButton = document.getElementById('next');
let currentImageIndex = 0;
let currentCar = null;

function openModal(car) {
    currentCar = car;
    currentImageIndex = 0;
    updateModalContent();
    modal.style.display = 'block';
}

function closeModalFunc() {
    modal.style.display = 'none';
}

function updateModalContent() {
    modalGallery.innerHTML = "";
    const { images, description } = currentCar;

    images.forEach((image, index) => {
        const img = document.createElement('img');
        img.src = image;
        img.alt = `Image ${index + 1}`;
        if (index === currentImageIndex) img.style.display = 'block';
        img.addEventListener('click', () => toggleZoom(img));
        modalGallery.appendChild(img);
    });

    const p = document.createElement('p');
    p.textContent = description;
    modalGallery.appendChild(p);
}

function showNextImage() {
    const { images } = currentCar;
    currentImageIndex = (currentImageIndex + 1) % images.length;
    updateModalContent();
}

function showPrevImage() {
    const { images } = currentCar;
    currentImageIndex = (currentImageIndex - 1 + images.length) % images.length;
    updateModalContent();
}

function toggleZoom(img) {
    img.classList.toggle('zoomed');
}

closeModal.onclick = closeModalFunc;
prevButton.onclick = showPrevImage;
nextButton.onclick = showNextImage;

window.onclick = (event) => {
    if (event.target === modal) {
        closeModalFunc();
    }
}

document.addEventListener('DOMContentLoaded', () => {
    const catalog = document.getElementById("catalog");

    function displayCars(category) {
        catalog.innerHTML = "";
        const filteredCars = cars.filter(car => car.category === category);
        if (filteredCars.length === 0) {
            catalog.innerHTML = "<p>Tidak ada mobil dalam kategori ini.</p>";
            return;
        }
        filteredCars.forEach(car => {
            const card = document.createElement("div");
            card.classList.add("card");

            card.innerHTML = `
                <img src="${car.images[0]}" alt="${car.name}" class="card-image">
                <div class="card-content">
                    <h3>${car.name}</h3>
                    <p>${car.description}</p>
                </div>
            `;

            card.querySelector('.card-image').addEventListener('click', () => openModal(car));

            catalog.appendChild(card);
        });
    }

    // Set kategori awal
    displayCars("CUSTOM LIMITED");

    document.querySelectorAll(".tab").forEach(tab => {
        tab.addEventListener("click", () => {
            document.querySelectorAll(".tab").forEach(t => t.classList.remove("active"));
            tab.classList.add("active");
            const category = tab.getAttribute("data-category");
            displayCars(category);
        });
    });
});
